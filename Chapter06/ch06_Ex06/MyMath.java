package ch06_Ex06;


public class MyMath {
	long add(long a, long b){
		long result= a + b;
		return result;
//		return a + b;		//위의 두줄을 한줄로 할 수 있음
	}
	long subtract(long a, long b){
		long result= a - b;
		return result;
	}
	long multiply(long a, long b){
		long result= a * b;
		return result;
	}
	double devide(double a, double b){
		double result= a / b;
		return result;
	}
}
